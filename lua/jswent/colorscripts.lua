local M = {}

M.random = function()
  local scripts = {}
  for k, v in pairs(M) do
    if k ~= "random" and type(v) == "table" and not v.disabled then
      scripts[k] = v
    end
  end
  local keys = {}
  for k in pairs(scripts) do
    table.insert(keys, k)
  end
  return scripts[keys[math.random(#keys)]]
end

